def main_menu():
    keyboard = []
    for i in range(1, 12):
        keyboard.append([InlineKeyboardButton(f"Номер {i}", callback_data=f"num_{i}")])
    return InlineKeyboardMarkup(keyboard)

def number_menu(current):
    keyboard = [
        [InlineKeyboardButton("⬅ Вернуться к списку", callback_data="back")]
    ]
    if current < 11:
        keyboard.append([InlineKeyboardButton("➡ Дальше", callback_data=f"num_{current+1}")])
    return InlineKeyboardMarkup(keyboard)

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(welcome_text)
    await update.message.reply_text("Выберите номер:", reply_markup=main_menu())

async def handle_buttons(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()

    data = query.data

    if data == "back":
        await query.message.reply_text("Выберите номер:", reply_markup=main_menu())
    elif data.startswith("num_"):
        number = int(data.split("_")[1])
        text = numbers[number]
        await query.message.reply_text(text, reply_markup=number_menu(number))

